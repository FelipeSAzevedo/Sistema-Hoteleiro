    public static void carregaComboReserva() {
        try {
            CB.removeAllItems();
            ReservaDAO PDAO = new ReservaDAO();
            lista = PDAO.listar();
        } catch (Exception ex) {
            System.out.println("problema");
        }

        for (int i = 0; i < lista.size(); i++) {
            CB.addItem(lista.get(i).getId_reserva());

        }
    }
    
    
