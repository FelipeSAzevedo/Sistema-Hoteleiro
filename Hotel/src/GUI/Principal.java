    private void CBItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_CBItemStateChanged
        
        posicao = (int) CB.getSelectedIndex();

        if (posicao != -1) {

            codigo = lista.get(posicao).getId_reserva();

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            T1.setText(sdf.format(lista.get(posicao).getData()));
            T2.setText(lista.get(posicao).getNome_cliente());
            T3.setText(Integer.toString(lista.get(posicao).getId_quarto()));
            T4.setText(Integer.toString(lista.get(posicao).getId_cliente()));
           
        }
    }//GEN-LAST:event_CBItemStateChanged

