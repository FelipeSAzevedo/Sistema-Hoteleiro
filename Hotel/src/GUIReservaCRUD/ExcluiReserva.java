    private void OKMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OKMouseClicked
        int vlr = JOptionPane.showConfirmDialog(rootPane, "Confirmar a exclusão", "Confirmação", 2);
        
        if (vlr == 0) {
            try {
                ReservaDAO PDAO = new ReservaDAO();
                PDAO.excluir(codigo);
                System.out.println("excluido com sucesso JAVA");
            } catch (Exception ex) {
                System.out.println("problema");
            }
            carregaComboReserva();
            dispose();
        }
            
        

    }//GEN-LAST:event_OKMouseClicked

    public static void carregaComboReserva() {
        try {
            CB.removeAllItems();
            ReservaDAO PDAO = new ReservaDAO();
            lista = PDAO.listar();
        } catch (Exception ex) {
            System.out.println("problema");
        }

        for (int i = 0; i < lista.size(); i++) {
            CB.addItem(lista.get(i).getId_reserva());

        }
    }
    
    
